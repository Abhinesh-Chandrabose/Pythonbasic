# Example for Dictionary in Python
student_info={ "Name":"Alice", "Age":20, "City":"London" }
print(student_info)
print(student_info["Name"])
print(student_info["Age"])
print(student_info["City"])
# Changing the Name in the dictionary
student_info["Name"]="Bob"
print("Updated Name:",student_info["Name"])
print("Complete student info:",student_info)
